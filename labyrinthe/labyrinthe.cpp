void testPoursuiteSDL()
{
	int dureeMax = 300;
	//lancement de la bibliotheque SDL
	SDL_Init(SDL_INIT_EVERYTHING);
	//creation de la fenetre windows et de l'affichage
	SDL_Window* win = SDL_CreateWindow("Labyrinthe", SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED, 800, 600, SDL_WINDOW_SHOWN | SDL_WINDOW_RESIZABLE | SDL_WINDOW_INPUT_GRABBED);
	SDL_Renderer* rend = SDL_CreateRenderer(win, -1, 0);
	//couleur noir mis en fond de la fenetre windows
	SDL_SetWindowGrab(win, SDL_TRUE);
	SDL_SetWindowIcon(win, IMG_Load("manekineko.png"));
	SDL_SetRenderDrawColor(rend, 0, 0, 0, 255);
	SDL_Texture* logo;
	SDL_Surface* temp = IMG_Load("logo.png");
	logo = SDL_CreateTextureFromSurface(rend, temp);
	SDL_FreeSurface(temp);
	SDL_RenderClear(rend);//effacement du rendu
	SDL_RenderCopy(rend, logo, NULL, NULL);
	SDL_RenderPresent(rend);//affichage
	SDL_Delay(10000);
	Labyrinthe laby(20, 30, 0.9, rend);
	SDL_SetWindowSize(win, 32 * (laby.getNbColonnes() + 2), 32 * (laby.getNbLignes() + 2));
	SDL_SetWindowPosition(win, SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED);
	SDL_SetRenderDrawColor(rend, 0, 0, 0, 255);

	//affichage du labyrinthe dans la console
	laby.affiche();
	SDL_RenderClear(rend);//effacement du rendu
	laby.afficheSDL(rend);//preparation de l'affichage dans la fenetre SDL
	SDL_RenderPresent(rend);//affichage
	laby.affiche(); SDL_RenderPresent(rend);

	int duree = laby.courseSDL(dureeMax, false, false, 1, 1, rend);
	if (duree > dureeMax)
	{
		cout << "Echec apres " << dureeMax << " etapes" << endl;
	}
	else
	{
		cout << "Succes apres " << duree << " etapes" << endl;
	}
	//destruction du rendu et de la fenetre windows
	SDL_DestroyTexture(logo);
	SDL_DestroyRenderer(rend);
	SDL_DestroyWindow(win);
	laby.~Labyrinthe();//destruction du labyrinthe
	SDL_Quit();//arret de la fenetre SDL
}


